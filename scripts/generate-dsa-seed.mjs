/** @param {string} name */
function leetcodeUrl(name) {
  const slug = name
    .toLowerCase()
    .replace(/'/g, '')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
  return `https://leetcode.com/problems/${slug}/`;
}

/** @param {string} name @param {'Easy'|'Medium'|'Hard'} difficulty */
function p(name, difficulty) {
  return {
    name,
    difficulty,
    leetcode_url: leetcodeUrl(name),
    solved: false,
    flagged_for_revision: false,
    notes: '',
  };
}

